//Métodos Strings

const text = "Adiós, mundo cruel";
text;

text.length; // --> Nos dice los caracteres que tiene el texto, por ejemplo los caracteres del Twitter (cantidad de caracteres del string)

// Transformar todos los caracteres a mayúsculas
text.toUpperCase();

//Transformar todos los caracteres a minúsculas
text.toLowerCase();

// Podemos usar uppercase y lowercase cuando queremos convertir un elemento en case insensitive, por ejemplo el e-mail, para que en un login funcione igual HOLA@HOLA.COM que Hola@Hola.com y hola@hola.com
